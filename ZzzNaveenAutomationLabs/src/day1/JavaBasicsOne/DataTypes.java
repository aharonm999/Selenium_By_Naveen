package day1.JavaBasicsOne;

public class DataTypes {

	public static void main(String[] args) {
		
	// Primitive Data Types
	// 1. Integer
		int i = 10;
		i=20;
		int j = 30;
		int k = -1;
		int l = 0;
		System.out.println(l); // we use this to print the values - Short cut: type 'syso' Ctrl+Space. 
		
	// 2. Double 
		double d = 12.33;
		double d1 = 34.4;
		double d2 = 100;
		
	// Char - any single digit value should be written in single quotes
		char c = 'a';
		char c1 = 'A';
		char c2 = '1';
		char c3 = '@';
		
	// Boolean
		boolean b = true;
		boolean b1 = false;
		
	// String - String is a class not a data type.
		String s = "Hello Boy";
		String s1 = "100";
		String s2 = "10.30";
		
	}

}
